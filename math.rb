def add(num1,num2,num3)
	result = num1 + num2 + num3
	return result
end
def subtract(num1,num2,num3)
	result = num1 - num2 - num3
	return result
end
def multiply(num1,num2,num3)
	result = num1 * num2 * num3
	return result
end
def divide(num1,num2,num3)
	result = num1 / num2 / num3
	return result
end